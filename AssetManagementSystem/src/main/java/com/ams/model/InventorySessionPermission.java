package com.ams.model;

public class InventorySessionPermission {
	private String permission_id;
	private String inventory_session_id;
	private String employee_id;
	public String getPermission_id() {
		return permission_id;
	}
	public void setPermission_id(String permission_id) {
		this.permission_id = permission_id;
	}
	public String getInventory_session_id() {
		return inventory_session_id;
	}
	public void setInventory_session_id(String inventory_session_id) {
		this.inventory_session_id = inventory_session_id;
	}
	public String getEmployee_id() {
		return employee_id;
	}
	public void setEmployee_id(String employee_id) {
		this.employee_id = employee_id;
	}
	

}
