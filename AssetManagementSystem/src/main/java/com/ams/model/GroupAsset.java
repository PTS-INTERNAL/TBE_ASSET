package com.ams.model;

public class GroupAsset {
	
	private String company_cd;
	
	private String group_id;
	
	private String group_desciption;
	private String group_name;
	public String getCompany_cd() {
		return company_cd;
	}
	public void setCompany_cd(String company_cd) {
		this.company_cd = company_cd;
	}
	public String getGroup_id() {
		return group_id;
	}
	public void setGroup_id(String group_id) {
		this.group_id = group_id;
	}
	public String getGroup_desciption() {
		return group_desciption;
	}
	public void setGroup_desciption(String group_desciption) {
		this.group_desciption = group_desciption;
	}
	public String getGroup_name() {
		return group_name;
	}
	public void setGroup_name(String group_name) {
		this.group_name = group_name;
	}
	
	

}
