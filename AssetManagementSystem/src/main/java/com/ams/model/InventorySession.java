package com.ams.model;

public class InventorySession {	
	String inventory_session_id;
	String inventory_session_name;
	String inventory_start_date;
	String inventory_end_date;
	String inventory_id;
	
	public String getInventory_session_id() {
		return inventory_session_id;
	}
	public void setInventory_session_id(String inventory_session_id) {
		this.inventory_session_id = inventory_session_id;
	}
	public String getInventory_session_name() {
		return inventory_session_name;
	}
	public void setInventory_session_name(String inventory_session_name) {
		this.inventory_session_name = inventory_session_name;
	}
	public String getInventory_start_date() {
		return inventory_start_date;
	}
	public void setInventory_start_date(String inventory_start_date) {
		this.inventory_start_date = inventory_start_date;
	}
	public String getInventory_end_date() {
		return inventory_end_date;
	}
	public void setInventory_end_date(String inventory_end_date) {
		this.inventory_end_date = inventory_end_date;
	}
	public String getInventory_id() {
		return inventory_id;
	}
	public void setInventory_id(String inventory_id) {
		this.inventory_id = inventory_id;
	}
}
